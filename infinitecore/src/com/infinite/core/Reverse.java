package com.infinite.core;

public class Reverse { // reverse number 50 to 1
	public static void main(String[] args){
		int i=50;// i initialised
		do{
			System.out.print(i+" "); // printing the number
			i--;// decrementing the present number
		}
		while(i>=1);// checking for i value
	}

}
