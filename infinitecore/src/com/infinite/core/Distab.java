package com.infinite.core;

public class Distab {// displaying 24 table
	public static void main(String[] args) {
		int i = 24;// initialising the i
		int j = 1;// initialising the j
		while (j <= 10) {
			int r = i * j;// code to print the table
			System.out.println(i + "*" + j + "=" + r);// displaying the table
														// format
			j++;// incrementing the j value
		}
	}

}
